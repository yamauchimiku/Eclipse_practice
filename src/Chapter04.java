
public class Chapter04 {
	public static void main(String[] args) {
		//自分の名前を変数に代入して出力
		String str = "miku";
		System.out.println(str);
		
		//好きな言葉を同じ変数に代入して出力
		str = "Hello";
		System.out.println(str);
		
		//ラッキーナンバーとして「int」型で宣言した定数を出力
		final int LUCKY_NUMBER = 7;
		System.out.println(LUCKY_NUMBER);
	}

}
